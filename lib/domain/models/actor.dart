class Actor {
  final int id;
  final String character;
  final String creditId;
  final int gender;
  final int castId;
  final String name;
  final int order;
  final String profilePath;

  Actor({required this.castId,
    required this.character,
    required this.creditId,
    required this.gender,
    required this.id,
    required this.name,
    required this.order,
    required this.profilePath,
  });
}
